package phoebe.eqx.smoi.bean.instance;

public class EQLRequestInstance {
	
	private String bsoId;
	private String bsoName;
	private String queryType;
	public String getBsoId() {
		return bsoId;
	}
	public void setBsoId(String bsoId) {
		this.bsoId = bsoId;
	}
	public String getBsoName() {
		return bsoName;
	}
	public void setBsoName(String bsoName) {
		this.bsoName = bsoName;
	}
	public String getQueryType() {
		return queryType;
	}
	public void setQueryType(String queryType) {
		this.queryType = queryType;
	}
	

	

}
